package Basics;
public class println{
    public static void main(String[] args) {
        // \n takes the place of print'ln'
        System.out.print("Look At Java Again\n");
        //Print line = line break
        System.out.println("Not to shabby");
        // \t will tab ln
        System.out.println("\t this is going well");
        //Sysout
        System.out.println("sysout shortcut to println");
        System.out.println();
        //
    }
}
